function show_loading(){
  document.getElementById("loadings").style.display="block";
}
function hide_loading(){
  document.getElementById("loadings").style.display="none";
  let hideEffect=setInterval(()=>{
    if(!document.getElementById("loadings").style.opacity){
      document.getElementById("loadings").style.opacity="1";
    }
    if(document.getElementById("loadings").style.opacity>"0"){
      document.getElementById("loadings").style.opacity="0.1";
    }
    else{
      clearInterval(hideEffect);
      document.getElementById("loadings").style.display="none";
    }
  },100);
}
const backToTopButton = document.querySelector("#back-to-top-btn");

window.addEventListener("scroll", scrollFunction);

function scrollFunction() {
  if (window.pageYOffset > 300) { // Show backToTopButton
    if(!backToTopButton.classList.contains("btnEntrance")) {
      backToTopButton.classList.remove("btnExit");
      backToTopButton.classList.add("btnEntrance");
      backToTopButton.style.display = "block";
    }
  }
  else { // Hide backToTopButton
    if(backToTopButton.classList.contains("btnEntrance")) {
      backToTopButton.classList.remove("btnEntrance");
      backToTopButton.classList.add("btnExit");
      setTimeout(function() {
        backToTopButton.style.display = "none";
      }, 250);
    }
  }
}

backToTopButton.addEventListener("click", smoothScrollBackToTop);

